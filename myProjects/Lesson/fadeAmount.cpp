#include <iostream>

int main() {
    int i{};
    int up{};
    int step{5};
    while (i <= 100) {
        ++i;
        up += step;
        if (up <= 0 || up >= 255) {
            step = -step;
        }
        std::cout << up << std::endl;
    }
    return 0;
}
