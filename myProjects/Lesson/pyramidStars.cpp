#include <iostream>

int main()
{
    const int step{5};
    for (int i = 1; i <= step; ++i) {
        for (int y = i; y < step; ++y) {
            std::cout << " ";
        }
        for (int x = 1; x < (i * 2); ++x) {
            std::cout << "*";
        }
        std::cout << "\n";
    }
    return 0;
}

