#include<iostream>
using namespace std;

int main() {
    float firstFloatNum = 2.3;
    float secondFloatNum = 1.1;
    float result = 0.0;
    result = firstFloatNum + secondFloatNum;
    cout << result << endl;
    result = firstFloatNum - secondFloatNum;
    cout << result << endl;
    result = firstFloatNum * secondFloatNum;
    cout << result << endl;
    result = firstFloatNum / secondFloatNum;
    char ch = 'a'; // Char - символьный тип данных
    cout << result << ch << endl;
    return 0;
}