#include <iostream>

int main() {
    using namespace std;
    int outer = 5;
    while (outer >= 1) {
        int inner = outer;
        while (inner >= 1) {
            cout << inner-- << " ";
        }
        cout << "\n";
        --outer;
    }
    return 0;
}
// Output:
/*
5 4 3 2 1
4 3 2 1
3 2 1
2 1
1
*/
