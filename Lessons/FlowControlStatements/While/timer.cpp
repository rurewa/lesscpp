#include <iostream>
#include <unistd.h>
using namespace std;

int main() {
	int sec = 0, min = 0, hour = 0;
	while (true) {
		sleep(1);
		cout << sec << " : " << min << " : " << hour << '\n';
        ++sec;
		while (sec >= 60) {
            sec = 0;
            cout << ++sec << " : " << ++min << " : " << hour << '\n';
			while (min > 60) {
                sec = 0, min = 0;
                cout << ++sec << " : " << min << " : " << ++hour << '\n';
				//++sec;
			}
		}
	}
	return 0;
}
