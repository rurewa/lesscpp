#include <iostream>
using namespace std;

int main() {
    int num = 8;
    int cound = 0;
    while (num > cound) {
        cout << num << " ";
        num -= 1;
    }
    return 0;
}

// Output:
/*
8 7 6 5 4 3 2 1
0
*/
