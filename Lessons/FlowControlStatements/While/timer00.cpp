#include <iostream>
#include <unistd.h>
using namespace std;

int main() {
	int sec = 0;
	int min = 0;
    int  hour = 0;
	while (true) {
		while (sec < 9) {
			cout << '0' << ++sec << " : " << '0' << min << " : " << '0' << hour << '\n';
			sleep(1);
			while (min >= 9) {
				cout << '0' << ++sec << " : " << min << " : " << '0' << hour << '\n';
				sleep(1);
			}
		}
		cout << ++sec << " : " << '0' << min << " : " << '0' << hour << '\n';
		if (sec >= 59) {
			sec = 0;
			cout << '0' << sec << " : " << '0' << ++min << " : " << '0' << hour << '\n';
			if (min >= 59) {
                sec = 0, min = 0;
                cout << '0' << sec << " : " << '0' << min << " : " << '0' << ++hour << '\n';
			}
		}
		sleep(1);
	}
	return 0;
}
