#include <iostream>
#include <unistd.h>
using namespace std;

int main() {
    int N = 8;
    for (int i = 0; i < N; cout << endl, ++i) {
        for (int k = 0; k < N; sleep(1), cout.flush(), ++k) {
            cout << ((i + k) % 2) << ' ';
        }
    }
    return 0;
}
