#include <iostream>
using namespace std;

int main() {
    for (char s = 'a'; s <= 'e'; cout << endl, ++s) {
        cout << s << ':';
        for (int i = 0; i <= 3; ++i) {
            cout << i; // 0123
        }
    }
    return 0;
}
