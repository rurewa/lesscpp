// -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//
#include <iostream>
using namespace std;

int main() {
    for (char c = 'a'; c <= 'e'; ++c) {
        cout << c << ":";
        for (int i = 0; i <= 3; ++i) {
            cout << i;
        }
        cout << "\n";
    }
    return 0;
}
// Output:
/*
a:0123
b:0123
c:0123
d:0123
e:0123
*/
