#include<iostream>
using namespace std;

int main() {
    cout << "\nВведите количество протонов\n";
    int userCount = 0;
    cin >> userCount;
    switch (userCount)
    {
        case 1:
            cout << "Это Водород\n";
            break;
        case 2:
            cout << "Это Гелий\n";
            break;
        case 3:
            cout << "Литий\n";
            break;
        default:
            cout << "Такой элемент я ещё не знаю!\n";
            break;
    }
    return 0;
}
