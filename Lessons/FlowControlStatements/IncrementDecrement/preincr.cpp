#include<iostream>
using namespace std;

int main() {
    int i = 0;
    cout << "Преинкремент: " << endl;
    cout << ++i << endl; // 0 + 1 = 1
    cout << ++i << endl; // 1 + 1 = 2
    cout << ++i << endl; // 2 + 1 = 3
    cout << ++i << endl;
    cout << ++i << endl;
    cout << ++i << endl;
    //-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    return 0;
}
// Output:
/*
Преинкремент:
1
2
3
4
5
6
*/
