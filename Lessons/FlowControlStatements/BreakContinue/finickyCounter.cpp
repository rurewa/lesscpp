// Использование операторов контроля управления потоком break и continue
// Эти операторы работают только в цикле!
// breakContinue.cpp While
// V 1.0
// -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=- //
#include<iostream>
using namespace std;

int main() {
    int count = 0;
    while (true) {
        ++count;
        if (count > 10) { // Доходим до 10
            break;  // И выходим (прыжок из цикла)
        }
        if (count == 5) {
            continue; // Пропускаем число 5 (прыжок из цикла)
            // Прыжок из цикла
        }
        cout << count << endl;
    }
    return 0;
}
