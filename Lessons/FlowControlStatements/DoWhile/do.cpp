#include <iostream>
using namespace std;

int main() {
    int count = 0;
    do {
        ++count;
        cout << count << " - хотя бы один раз..." << "\n";
    } while (count < 0);
   /*
    while (count < 0) {
        ++count;
        cout << count << "\n";
    }
    */
    return 0;
}
// Output:
/*
1 - хотя бы один раз...
*/
