// Пример вывода 100-а псевдослучайных чисел. Вариант с функцией rand();
// V 1.0
// -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//
#include <iostream>
#include <cstdlib>

int main() {
    using namespace std;
    srand(4541); // устанавливаем стартовое значение - 4 541
    for (int count = 0; count < 100; ++count) {
        cout << rand() << "\t";
        // Если вывели 5 чисел, то вставляем символ новой строки
        if ((count + 1) % 5 == 0) {
            cout << "\n";
        }
    }
    return 0;
}
