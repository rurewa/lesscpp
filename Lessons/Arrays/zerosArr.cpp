#include <iostream>
using namespace std;

int main() {
    int arr[5] = {0}; // Инициализация массива нулями
    for (auto i : arr) {
        cout << i << ' ';
    }
    cout << '\n';
    return 0;
}
/* Output:
0 0 0 0 0
*/
