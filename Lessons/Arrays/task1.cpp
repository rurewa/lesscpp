// Пример вывода содержимого массива. Заполнение массива
// V 1.0
// -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//
#include <iostream>
using namespace std;

int main() {
    const int arr[] = {45, 35, 45, 98};
    for (int index = 0; index <= 3; ++index) {
        cout << arr[index] << ' ';
    }
    cout << endl;
    int foo[3] = {};
    cout << (foo[0] = 25) << endl;
    cout << foo[1] << endl;
    cout << foo[2] << endl;
    cout << foo << endl;
    return 0;
}
// Output:
/*
45 35 45 98
25
0
0
0x7fffadcbb390
*/
