#include <iostream>
using namespace std;

int main() {
    char chess[8][8];
    for(int i = 0; i < 8; ++i){
        for(int j = 0; j < 8; ++j){
            if((i + j) % 2 == 0) {
                chess[i][j] = ' ';
            }
            else {
                chess[i][j] = '#';
            }
        }
    }
    cout<<"Результат: " << endl;
    for(int i = 0; i < 8; ++i){
        for(int j = 0; j < 8; ++j){
            cout << "\e[47m" << chess[i][j] << "\e[0m";
        }
        cout << endl;
    }
    return 0;
}
