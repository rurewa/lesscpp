#include <iostream>
using namespace std;

int main() {
    int array[2][3] {{7, -5, 9}, {6, 4, 8}};
    cout << "[0][0] - " << array[0][0] << endl;
    cout << "[0][1] - " << array[0][1] << endl;
    cout << "[0][2] - " << array[0][2] << endl;
    cout << "[1][0] - " << array[1][0] << endl;
    cout << "[1][1] - " << array[1][1] << endl;
    cout << "[1][2] - " << array[1][2] << endl;
    return 0;
}
// Output:
/*
[0][0] - 7
[0][1] - -5
[0][2] - 9
[1][0] - 6
[1][1] - 4
[1][2] - 8
*/
