#include <iostream>
using namespace std;

// types.cpp

int main() {
    cout << "Типы данных\n";
    // short - маленький целочисленный тип
    // int - средний целочисленный тип
    // long - большой целочисленный тип
    // 0 1 2 35 66 100
    cout << "Тип short: " << sizeof(short) << " байта" << '\n';
    cout << "Тип int: " << sizeof(int) << " байта" << '\n';
    return 0;
}